    private void holdDiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_holdDiceActionPerformed
        // TODO add your handling code here:
        if (currentPlayer==true){
            total+=perRole;
            perRole=0;
            currentPlayer=false;
            playerOnePointsTotal.setText(Integer.toString(total));
            player2TF.setText("");
            //if player one has more than 99 points display player1+wins
            if (total > 99){
                playerOnePointsTotal.setText(player1+"Wins");
                //add total points of player one and player two
                // to the arraylist called playerScore
                playerScore.add(total,totalTwo);
            }
        }
        if(currentPlayer==false){
            totalTwo+=perRoleTwo;
            perRoleTwo=0;
            currentPlayer=true;
            playerTwoTotal.setText(Integer.toString(totalTwo));
            player1TF.setText("");
            //if player Two has more than 99 points display player2+wins
            if (totalTwo>99){
                playerTwoTotal.setText(player2+("wins"));
                //add total points of player one and player two
                // to the arraylist called playerScore
                playerScore.add(total,totalTwo);
            }
        }
    }//GEN-LAST:event_holdDiceActionPerformed

