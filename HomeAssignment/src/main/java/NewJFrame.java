    private void rollDiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rollDiceActionPerformed
        
        // ROLL DICE
       //roll random number
            Random r = new Random();
            //currentplayer==true means player one
            //currentplayer==false means player two
            
            if(currentPlayer==true){
                int randomNum =r.nextInt(1,7);
                perRole+=randomNum;
                 //display text in player 1 text field
                player1TF.setText(player1TF.getText()+randomNum+"\n");
                currentPlayerLbl.setText(player1);
                
              //if dice roll is 1 for player 1 >> do that   
            if (randomNum==1){
                //Remove points from player1
                //player to false
                
                currentPlayer=false;
                perRole=0;
                total=0;
                player2TF.setText("");
            }
                
            }
            
            //if dice roll is 1 for player 2 to do that
            if (currentPlayer==false){
                
                int randomNum =r.nextInt(1,7);
                perRoleTwo+=randomNum;
                //display number in player 2 text field
                player2TF.setText(player2TF.getText()+randomNum+"\n");
                currentPlayerLbl.setText("robot");
             
                if (randomNum==1){
                //Remove points from player1
                //player to false
                
                currentPlayer=true;
                perRoleTwo=0;
                totalTwo=0;
                player1TF.setText("");
            }
            }
                  
    }//GEN-LAST:event_rollDiceActionPerformed

